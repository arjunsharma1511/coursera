// disable right click
document.addEventListener('contextmenu', event => event.preventDefault());
// disable ctrl+shift+i
document.onkeydown = function(e) {
    if (e.ctrlKey && e.shiftKey && e.keyCode == 'I'.charCodeAt(0)) {
        return false;
    } else {
        return true;
    }
}
